function countCharacters(words: string[], chars: string): number {
  let count = 0;
  for (let i = 0; i < words.length; i++) {
    let charCopy: (string | null)[] = chars.split("");
    let word = words[i];
    let EveryLetter = Array.from(word).every((ltr, idx) => {
      let check = charCopy.includes(ltr);
      charCopy[charCopy.indexOf(ltr)] = null;
      return check ? true : false;
    })
    EveryLetter ? count += word.length : ""
  }
  return count;
};

